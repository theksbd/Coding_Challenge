package com.leetcode;

public class AddTwoIntegers {
    public int sum(int num1, int num2) {
        return num1 + num2;
    }

    public static void main(String[] args) {
        AddTwoIntegers a = new AddTwoIntegers();
        System.out.println(a.sum(5, 3));
    }
}
